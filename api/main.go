// AUTO-GENERATE DEFAULT API KEY
// ---------------------------------------------------------------------------

func autoGenerateDefaultAPIKey() {
	var count int64
	db.DB.Model(&models.APIKey{}).Where("status = ?", "ACTIVE").Count(&count)
	if count > 0 {
		return // Keys already exist
	}

	// Generate a default key
	rawKey := uuid.New().String() + "-" + uuid.New().String()
	hash := sha256.Sum256([]byte(rawKey))
	keyHash := fmt.Sprintf("%x", hash[:])

	apiKey := models.APIKey{
		ID:          uuid.New().String(),
		Name:        "default",
		KeyHash:     keyHash,
		KeyPrefix:   rawKey[:8],
		Permissions: "read,write",
		RateLimit:   100,
		Status:      "ACTIVE",
	}

	if err := db.DB.Create(&apiKey).Error; err != nil {
		logger.Log.Error("Failed to create default API key", zap.Error(err))
		return
	}

	handlers.SyncAPIKeysToRedis()

	// Store raw key in Redis for mail-edge to use as Bearer token
	db.Redis.Set(context.Background(), "system:api_token", rawKey, 0)

	logger.Log.Info("═══════════════════════════════════════════════════════")
	logger.Log.Info("DEFAULT API KEY GENERATED (save this — shown only once!)")
	logger.Log.Info("API_KEY: " + rawKey)
	logger.Log.Info("═══════════════════════════════════════════════════════")
}

