// apiTokenMiddleware validates API key against Redis (synced from DB)
// Accepts: X-API-Key: <raw_key>  OR  Authorization: Bearer <raw_key>
func apiTokenMiddleware(c *fiber.Ctx) error {
	// Extract token from either header
	token := c.Get("X-API-Key")
	if token == "" {
		auth := c.Get("Authorization")
		if strings.HasPrefix(auth, "Bearer ") {
			token = auth[7:]
		}
	}

	if token == "" {
		return c.Status(fiber.StatusUnauthorized).JSON(fiber.Map{
			"error": "API key required",
		})
	}

	// Hash the incoming key and check against Redis set
	// Optimized: hex.EncodeToString avoids fmt.Sprintf allocation
	hash := sha256.Sum256([]byte(token))
	keyHash := hex.EncodeToString(hash[:])

	ctx := context.Background()
	isMember, err := db.Redis.SIsMember(ctx, "system:api_key_hashes", keyHash).Result()
	if err != nil || !isMember {
		return c.Status(fiber.StatusUnauthorized).JSON(fiber.Map{
			"error": "Invalid API key",
		})
	}

	// Check if this key has internal (rate-limit bypass) flag
	meta, _ := db.Redis.HGet(ctx, "system:api_key_meta", keyHash).Result()
	if meta == "internal" {
		c.Locals("is_internal_key", true)
	}

	return c.Next()
}

// adminAuthMiddleware validates session token (Bearer header or ?token= query param) for admin panel access
func adminAuthMiddleware(c *fiber.Ctx) error {
	if cachedAdminAPIKey == "" {
		logger.Log.Error("ADMIN_API_KEY not configured — rejecting admin requests")
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"error": "Admin panel not configured",
		})
	}

	var token string

	// 1) Check Authorization: Bearer <token>
	auth := c.Get("Authorization")
	if strings.HasPrefix(auth, "Bearer ") {
		token = auth[7:] // faster than TrimPrefix
	}

	// 2) Fallback: ?token= query param (for EventSource/SSE which can't send headers)
	if token == "" {
		token = c.Query("token")
	}

	if token != "" {
		if username, ok := handlers.ValidateSessionToken(token, cachedAdminAPIKey); ok {
			c.Locals("admin_user", username)
			return c.Next()
		}
	}

	return c.Status(fiber.StatusUnauthorized).JSON(fiber.Map{
		"error": "Invalid or expired session",
	})
}

func generateSecureToken(length int) string {
	b := make([]byte, length)
	rand.Read(b)
	return hex.EncodeToString(b)
}
