// Cached at startup — avoid os.Getenv() on every admin request
var cachedAdminAPIKey string

func main() {
	// Load configuration from environment variables
	cfg := config.Load()

	if err := logger.InitLogger("api"); err != nil {
		log.Fatalf("Failed to initialize logger: %v", err)
	}
	defer logger.Log.Sync()

	// Initialize databases
	dbURL := os.Getenv("DATABASE_URL")
	if dbURL == "" {
		logger.Log.Fatal("DATABASE_URL environment variable is required")
	}

	if err := db.InitPostgres(dbURL); err != nil {
		logger.Log.Fatal("Failed to initialize PostgreSQL", zap.Error(err))
	}

	redisURL := os.Getenv("REDIS_URL")
	if redisURL == "" {
		logger.Log.Fatal("REDIS_URL environment variable is required")
	}
	if err := db.InitRedis(redisURL); err != nil {
		logger.Log.Fatal("Failed to initialize Redis", zap.Error(err))
	}

	// AutoMigrate
	if err := models.Migrate(db.DB); err != nil {
		logger.Log.Fatal("Failed to migrate database", zap.Error(err))
	}

	// Auto-register this server as primary node
	autoRegisterPrimaryNode()

	// Preload default settings for Webhook, TTL, Rate Limit
	preloadDefaultSettings()

	// Sync API keys from DB to Redis + auto-generate if none exist
	handlers.SyncAPIKeysToRedis()
	autoGenerateDefaultAPIKey()

	// Cache admin key at startup
	cachedAdminAPIKey = os.Getenv("ADMIN_API_KEY")

	app := fiber.New(fiber.Config{
		BodyLimit:             cfg.API.BodyLimitBytes(),
		Concurrency:           cfg.API.Concurrency,
		ReadTimeout:           cfg.API.ReadTimeout,
		WriteTimeout:          cfg.API.WriteTimeout,
		IdleTimeout:           cfg.API.IdleTimeout,
		DisableStartupMessage: true,
		ServerHeader:          "",
		AppName:               "",
		ReadBufferSize:        8192,  // 8KB — reduces syscalls vs default 4KB
		WriteBufferSize:       8192,
	})

	// Startup resource banner — essential for debugging in production
	logger.Log.Info("🚀 TempMail API starting",
		zap.Int("cpus", runtime.NumCPU()),
		zap.Int("gomaxprocs", runtime.GOMAXPROCS(0)),
		zap.String("go_version", runtime.Version()),
		zap.Int("fiber_concurrency", cfg.API.Concurrency),
		zap.Int("db_max_open", cfg.DB.MaxOpenConns),
		zap.Int("db_max_idle", cfg.DB.MaxIdleConns),
		zap.Int("redis_pool", cfg.Redis.PoolSize),
		zap.String("read_timeout", cfg.API.ReadTimeout.String()),
		zap.String("write_timeout", cfg.API.WriteTimeout.String()),
		zap.String("idle_timeout", cfg.API.IdleTimeout.String()),
	)

	// -----------------------------------------------------------------------
	// GLOBAL MIDDLEWARES
	// -----------------------------------------------------------------------

	// Panic recovery
	app.Use(recover.New())

	// Request ID for audit trails
	app.Use(requestid.New())

	// Access logging
	app.Use(fiberLogger.New(fiberLogger.Config{
		Format:     "${time} | ${status} | ${latency} | ${ip} | ${method} ${path} | ${reqHeader:X-Request-Id}\n",
		TimeFormat: "2006-01-02 15:04:05",
	}))

	// Security headers
	app.Use(func(c *fiber.Ctx) error {
		c.Set("X-Content-Type-Options", "nosniff")
		c.Set("X-Frame-Options", "DENY")
		c.Set("X-XSS-Protection", "1; mode=block")
		c.Set("Referrer-Policy", "strict-origin-when-cross-origin")
		c.Set("Content-Security-Policy", "default-src 'self'; script-src 'self' 'unsafe-inline'; style-src 'self' 'unsafe-inline'; style-src-elem 'self' 'unsafe-inline' https://fonts.googleapis.com; img-src 'self' data: https: http:; font-src 'self' data: https://fonts.gstatic.com https://fonts.googleapis.com; connect-src 'self'; frame-src 'self'")
		c.Set("Strict-Transport-Security", "max-age=63072000; includeSubDomains")
		return c.Next()
	})

	// CORS — optional, only enabled when FRONTEND_URL is set.
	frontendURL := os.Getenv("FRONTEND_URL")
	if frontendURL != "" {
		app.Use(cors.New(cors.Config{
			AllowOrigins:     frontendURL,
			AllowMethods:     "GET,POST,PUT,DELETE,OPTIONS",
			AllowHeaders:     "Origin,Content-Type,Accept,Authorization,X-API-Key,X-Request-Id",
			AllowCredentials: true,
			MaxAge:           3600,
		}))
		logger.Log.Info("CORS enabled", zap.String("origins", frontendURL))
	}

	// -----------------------------------------------------------------------
	// PUBLIC ROUTES (rate limited per IP — open to the world)
	// -----------------------------------------------------------------------
	publicLimiter := limiter.New(limiter.Config{
		Max:        cfg.API.PublicRateLimitPerMin,
		Expiration: 1 * time.Minute,
		KeyGenerator: func(c *fiber.Ctx) string {
			return c.IP()
		},
		LimitReached: func(c *fiber.Ctx) error {
			logger.Log.Warn("Rate limit exceeded", zap.String("ip", c.IP()))
			return c.Status(fiber.StatusTooManyRequests).JSON(fiber.Map{
				"error": "Rate limit exceeded. Try again later.",
			})
		},
	})

	app.Get("/health", publicLimiter, func(c *fiber.Ctx) error {
		return c.JSON(fiber.Map{"status": "ok"})
	})

	// Public: server info for DNS setup instructions (Web App uses this)
	app.Get("/api/server-info", publicLimiter, handlers.HandleServerInfo)

	// -----------------------------------------------------------------------
	// INTERNAL ROUTES — mail-edge → api (Bearer API_TOKEN)
	// -----------------------------------------------------------------------
	internal := app.Group("/internal")
	internal.Use(apiTokenMiddleware)
	internal.Post("/mail/ingest", handlers.HandleMailIngest)

	// -----------------------------------------------------------------------
	// SDK ROUTES — frontend → api (X-API-Key or Bearer API_TOKEN)
	// -----------------------------------------------------------------------
	v1 := app.Group("/v1")
	v1.Use(apiTokenMiddleware)
	v1.Post("/mailbox/create", handlers.HandleCreateMailbox)
	v1.Get("/mailbox/count", handlers.HandleMailboxCount)
	v1.Get("/mailbox/:id", handlers.HandleGetMailbox)
	v1.Get("/mailbox/:id/messages", handlers.HandleGetMessages)
	v1.Patch("/mailbox/:id", handlers.HandlePatchMailbox)
	v1.Delete("/mailbox/:id", handlers.HandleDeleteMailbox)
	v1.Get("/message/:id", handlers.HandleGetMessage)
	v1.Delete("/message/:id", handlers.HandleDeleteMessage)
	v1.Get("/attachment/:id", handlers.HandleDownloadAttachment)
	v1.Get("/domains", handlers.HandleListDomains)

	// -----------------------------------------------------------------------
	// ADMIN ROUTES
	// -----------------------------------------------------------------------

	// Login-specific limiter — strict to prevent brute-force
	loginLimiter := limiter.New(limiter.Config{
		Max:        cfg.API.LoginRateLimitPerMin,
		Expiration: 1 * time.Minute,
		KeyGenerator: func(c *fiber.Ctx) string {
			return c.IP()
		},
		LimitReached: func(c *fiber.Ctx) error {
			logger.Log.Warn("Login rate limit exceeded", zap.String("ip", c.IP()))
			return c.Status(fiber.StatusTooManyRequests).JSON(fiber.Map{
				"error": "Too many login attempts. Try again later.",
			})
		},
	})

	admin := app.Group("/admin")

	// Login route — public (no auth), strict rate limit
	admin.Post("/login", loginLimiter, handlers.HandleAdminLogin)

	// Protected admin routes — require Bearer session token
	admin.Use(adminAuthMiddleware)
	admin.Get("/dashboard", handlers.HandleAdminDashboard)
	admin.Get("/metrics", handlers.HandleAdminMetrics)

	// Domains CRUD
	admin.Get("/domains", handlers.HandleAdminDomains)
	admin.Get("/domains/dns-check", handlers.HandleDNSCheck)
	admin.Post("/domains", handlers.HandleAdminCreateDomain)
	admin.Put("/domains/:id", handlers.HandleAdminUpdateDomain)
	admin.Delete("/domains/:id", handlers.HandleAdminDeleteDomain)

	// Nodes CRUD
	admin.Get("/nodes", handlers.HandleAdminNodes)
	admin.Post("/nodes", handlers.HandleAdminCreateNode)
	admin.Put("/nodes/:id", handlers.HandleAdminUpdateNode)
	admin.Delete("/nodes/:id", handlers.HandleAdminDeleteNode)
	admin.Post("/nodes/:id/detect-hostname", handlers.HandleDetectHostname)

	// Filters CRUD
	admin.Get("/filters", handlers.HandleAdminFilters)
	admin.Post("/filters", handlers.HandleAdminCreateFilter)
	admin.Put("/filters/:id", handlers.HandleAdminUpdateFilter)
	admin.Delete("/filters/:id", handlers.HandleAdminDeleteFilter)

	// Mailboxes
	admin.Get("/mailboxes", handlers.HandleAdminMailboxes)
	admin.Post("/mailboxes/quick-create", handlers.HandleAdminQuickCreateMailbox)
	admin.Delete("/mailboxes/:id", handlers.HandleAdminDeleteMailbox)

	// Messages
	admin.Get("/messages", handlers.HandleAdminMessages)
	admin.Get("/messages/:id", handlers.HandleAdminMessageDetail)
	admin.Delete("/messages/:id", handlers.HandleAdminDeleteMessage)
	admin.Post("/messages/bulk-delete", handlers.HandleAdminBulkDeleteMessages)
	admin.Get("/attachment/:id", handlers.HandleDownloadAttachment)

	// Settings + Export/Import
	admin.Get("/settings", handlers.HandleAdminGetSettings)
	admin.Post("/settings", handlers.HandleAdminUpdateSettings)
	admin.Get("/export", handlers.HandleAdminExport)
	admin.Post("/import", handlers.HandleAdminImport)

	// API Keys CRUD
	admin.Get("/api-keys", handlers.HandleAdminAPIKeys)
	admin.Post("/api-keys", handlers.HandleAdminCreateAPIKey)
	admin.Put("/api-keys/:id", handlers.HandleAdminUpdateAPIKey)
	admin.Delete("/api-keys/:id", handlers.HandleAdminDeleteAPIKey)
	admin.Post("/api-keys/:id/roll", handlers.HandleAdminRollAPIKey)

	// Audit + Webhook
	admin.Get("/audit-log", handlers.HandleAdminAuditLog)
	admin.Post("/webhook-test", handlers.HandleAdminWebhookTest)

	// SSE — real-time events for admin panel (no polling)
	// Auth handled by adminAuthMiddleware (supports query param ?token= for EventSource)
	admin.Get("/events", func(c *fiber.Ctx) error {
		c.Set("Content-Type", "text/event-stream")
		c.Set("Cache-Control", "no-cache")
		c.Set("Connection", "keep-alive")
		c.Set("X-Accel-Buffering", "no")

		sub := db.Redis.Subscribe(context.Background(), "mail:events")
		ch := sub.Channel()

		c.Context().SetBodyStreamWriter(func(w *bufio.Writer) {
			defer sub.Close()

			// Send initial keepalive
			if _, err := fmt.Fprintf(w, ": connected\n\n"); err != nil {
				return
			}
			if err := w.Flush(); err != nil {
				return
			}

			ticker := time.NewTicker(12 * time.Second)
			defer ticker.Stop()

			for {
				select {
				case <-c.Context().Done():
					return
				case msg, ok := <-ch:
					if !ok {
						return
					}
					if _, err := fmt.Fprintf(w, "event: %s\ndata: {}\n\n", msg.Payload); err != nil {
						return
					}
					if err := w.Flush(); err != nil {
						return
					}
				case <-ticker.C:
					if _, err := fmt.Fprintf(w, ": keepalive\n\n"); err != nil {
						return
					}
					if err := w.Flush(); err != nil {
						return
					}
				}
			}
		})
		return nil
	})

	port := cfg.API.Port

	// Serve admin UI at a fixed path from .env
	adminPanelPath := os.Getenv("ADMIN_PANEL_PATH")
	if adminPanelPath == "" {
		logger.Log.Warn("ADMIN_PANEL_PATH not set — admin UI disabled. Set it in .env for a fixed, shareable path.")
	} else {
		app.Get("/"+adminPanelPath, func(c *fiber.Ctx) error {
			return c.SendFile("./admin-ui/index.html")
		})
		app.Get("/"+adminPanelPath+"/*", func(c *fiber.Ctx) error {
			file := c.Params("*")
			if file == "" {
				file = "index.html"
			}
			// Prevent path traversal — resolve and verify the path stays inside admin-ui/
			cleaned := filepath.Clean(filepath.Join("admin-ui", file))
			if !strings.HasPrefix(cleaned, "admin-ui"+string(filepath.Separator)) && cleaned != "admin-ui" {
				return c.Status(403).JSON(fiber.Map{"error": "forbidden"})
			}
			return c.SendFile("./" + cleaned)
		})
		logger.Log.Info("Admin panel available",
			zap.String("url", "http://localhost:"+port+"/"+adminPanelPath+"/"),
		)
	}

	// ---------------------------------------------------------------------------
	// GRACEFUL SHUTDOWN — drain in-flight requests before stopping
	// ---------------------------------------------------------------------------
	quit := make(chan os.Signal, 1)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)

	go func() {
		logger.Log.Info("Starting API server", zap.String("port", port))
		if err := app.Listen(":" + port); err != nil {
			logger.Log.Fatal("API server failed", zap.Error(err))
		}
	}()

	<-quit
	logger.Log.Info("Shutting down gracefully (10s timeout)...")
	if err := app.ShutdownWithTimeout(10 * time.Second); err != nil {
		logger.Log.Error("Forced shutdown", zap.Error(err))
	}
	logger.Log.Info("Server stopped")
}

