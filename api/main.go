// AUTO-REGISTER PRIMARY NODE
// ---------------------------------------------------------------------------

func autoRegisterPrimaryNode() {
	var count int64
	db.DB.Model(&models.MailNode{}).Count(&count)
	if count > 0 {
		return // Nodes already exist, skip auto-registration
	}

	// Auto-detect public IP — no env vars needed
	ip := detectPublicIP()
	if ip == "" {
		logger.Log.Warn("Could not detect public IP — skipping auto-register primary node")
		return
	}

	// Try reverse DNS to auto-detect hostname
	hostname := detectHostname(ip)

	node := models.MailNode{
		ID:        uuid.New().String(),
		Name:      "primary",
		Hostname:  hostname,
		IPAddress: ip,
		Region:    "",
		Status:    "ACTIVE",
	}

	if err := db.DB.Create(&node).Error; err != nil {
		logger.Log.Warn("Failed to auto-register primary node", zap.Error(err))
		return
	}

	if hostname != "" {
		logger.Log.Info("Primary node auto-registered (hostname detected via PTR)",
			zap.String("name", node.Name),
			zap.String("hostname", hostname),
			zap.String("ip", ip),
		)
	} else {
		logger.Log.Info("Primary node auto-registered (no PTR record — set hostname in Admin Panel)",
			zap.String("name", node.Name),
			zap.String("ip", ip),
		)
	}
}

func detectPublicIP() string {
	// Try these services in order
	services := []string{
		"https://api.ipify.org",
		"https://ifconfig.me/ip",
		"https://icanhazip.com",
	}
	client := &gonet.Client{Timeout: 5 * time.Second}
	for _, url := range services {
		resp, err := client.Get(url)
		if err != nil {
			continue
		}
		body, _ := io.ReadAll(resp.Body)
		resp.Body.Close()
		ip := strings.TrimSpace(string(body))
		if ip != "" {
			return ip
		}
	}
	return ""
}

// detectHostname does reverse DNS (PTR) lookup on an IP to find hostname.
// Returns empty string if no PTR record found.
func detectHostname(ip string) string {
	hosts, err := net.LookupAddr(ip)
	if err != nil || len(hosts) == 0 {
		return ""
	}
	// PTR records end with a dot — trim it
	h := strings.TrimSuffix(hosts[0], ".")
	// Skip generic ISP/cloud hostnames (they're not useful for MX)
	if strings.Contains(h, "compute") || strings.Contains(h, "bc.googleusercontent") {
		return ""
	}
	return h
}

